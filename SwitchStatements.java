public class SwitchStatements {
    public static void main(String args[]){
        int day =2; // 1- Monday
        switch (day) {
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            default:
                System.out.println("Sunday");
        }
    }
}
