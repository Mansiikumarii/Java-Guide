public class FoorLoop{
    public static void main(String[] args) {
        for(int i=1; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("odd numbers:");
        for(int j=1; j<=10; j++){
            System.out.println(2*j+1);
        }
        System.out.println("countdown:");
        for(int k =10; k>=1; k--){
            System.out.println(k);
        }
    }
}
